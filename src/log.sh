#!/bin/bash

__debug() {
    if $VERBOSE
    then
        echo -e '\033[1;35m ==> [DEBUG]: \033[0m' $1
    fi
}

__info() {
    echo -e '\033[1;34m ==> [INFO]: \033[0m' $1
}

__warn() {
    echo -e '\033[1;33m ==> [WARN]: \033[0m' $1
}

__error() {
    echo -e '\033[1;31m ==> [ERROR]: \033[0m' $1
}

__title() {
    echo -e '\033[1;32m===================================================='
    echo $1
    echo -e '====================================================\033[0m'
}

